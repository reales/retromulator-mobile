inline std::array<double, N> build_rhs(double input, double input_prev, const CircuitState& state)
{
    std::array<double, N> rhs = RHS_CONST;

    rhs[0] += state.a_neg[0][0] * state.v_prev[0] + state.a_neg[0][1] * state.v_prev[1];
    rhs[1] += state.a_neg[1][0] * state.v_prev[0]
        + state.a_neg[1][1] * state.v_prev[1]
        + state.a_neg[1][2] * state.v_prev[2];
    rhs[2] += state.a_neg[2][1] * state.v_prev[1]
        + state.a_neg[2][2] * state.v_prev[2]
        + state.a_neg[2][3] * state.v_prev[3]
        + state.a_neg[2][4] * state.v_prev[4];
    rhs[3] += state.a_neg[3][2] * state.v_prev[2]
        + state.a_neg[3][3] * state.v_prev[3]
        + state.a_neg[3][4] * state.v_prev[4]
        + state.a_neg[3][7] * state.v_prev[7]
        + state.a_neg[3][11] * state.v_prev[11];
    rhs[4] += state.a_neg[4][2] * state.v_prev[2]
        + state.a_neg[4][3] * state.v_prev[3]
        + state.a_neg[4][4] * state.v_prev[4]
        + state.a_neg[4][7] * state.v_prev[7];
    rhs[5] += state.a_neg[5][5] * state.v_prev[5] + state.a_neg[5][6] * state.v_prev[6];
    rhs[6] += state.a_neg[6][5] * state.v_prev[5]
        + state.a_neg[6][6] * state.v_prev[6]
        + state.a_neg[6][10] * state.v_prev[10];
    rhs[7] += state.a_neg[7][3] * state.v_prev[3]
        + state.a_neg[7][4] * state.v_prev[4]
        + state.a_neg[7][7] * state.v_prev[7]
        + state.a_neg[7][10] * state.v_prev[10];
    rhs[8] += state.a_neg[8][8] * state.v_prev[8] + state.a_neg[8][9] * state.v_prev[9];
    rhs[9] += state.a_neg[9][8] * state.v_prev[8] + state.a_neg[9][9] * state.v_prev[9];
    rhs[10] += state.a_neg[10][6] * state.v_prev[6]
        + state.a_neg[10][7] * state.v_prev[7]
        + state.a_neg[10][10] * state.v_prev[10];

    // Add N_i * i_nl_prev contribution
    rhs[2] += N_I[0][2] * state.i_nl_prev[0];
    rhs[2] += N_I[1][2] * state.i_nl_prev[1];
    rhs[4] += N_I[1][4] * state.i_nl_prev[1];
    rhs[4] += N_I[2][4] * state.i_nl_prev[2];
    rhs[5] += N_I[1][5] * state.i_nl_prev[1];
    rhs[7] += N_I[2][7] * state.i_nl_prev[2];
    rhs[8] += N_I[2][8] * state.i_nl_prev[2];

    // Add input contribution (Thevenin)
    rhs[INPUT_NODE] += (input + input_prev) / INPUT_RESISTANCE;

    return rhs;
}

inline std::array<double, N> mat_vec_mul_s(const std::array<double, N>& rhs, const CircuitState& state)
{
    std::array<double, N> result = {};
    for (size_t i = 0; i < N; ++i) {
        double sum = 0.0;
        for (size_t j = 0; j < N; ++j) {
            sum += state.s[i][j] * rhs[j];
        }
        result[i] = sum;
    }
    return result;
}

inline std::array<double, M> extract_controlling_voltages(const std::array<double, N>& v_pred)
{
    return {{-v_pred[2], v_pred[2] - v_pred[5], v_pred[4] - v_pred[8]}};
}

// =============================================================================
// NONLINEAR SOLVER
// =============================================================================

inline std::array<double, M> solve_nonlinear(
    const std::array<double, M>& p,
    const std::array<std::array<double, M>, M>& k_eff,
    CircuitState& state)
{
    constexpr size_t MAX_ITER = 200;
    constexpr double TOL = 1.00000000000000006e-9;
    constexpr double SINGULARITY_THRESHOLD = 1e-15;

    std::array<double, M> i_nl = {};
    for (size_t i = 0; i < M; ++i) {
        i_nl[i] = 2.0 * state.i_nl_prev[i] - state.i_nl_prev_prev[i];
    }

    for (size_t iter = 0; iter < MAX_ITER; ++iter) {
        double v_d0 = p[0] + k_eff[0][0] * i_nl[0] + k_eff[0][1] * i_nl[1] + k_eff[0][2] * i_nl[2];
        double v_d1 = p[1] + k_eff[1][0] * i_nl[0] + k_eff[1][1] * i_nl[1] + k_eff[1][2] * i_nl[2];
        double v_d2 = p[2] + k_eff[2][0] * i_nl[0] + k_eff[2][1] * i_nl[1] + k_eff[2][2] * i_nl[2];

        double i_dev0 = diode_current(v_d0, state.device_0_is, state.device_0_n_vt);
        double jdev_0_0 = diode_conductance(v_d0, state.device_0_is, state.device_0_n_vt);

        double vbe_1 = v_d1 * DEVICE_1_SIGN;
        double exp_be_1 = fast_exp(vbe_1 / (DEVICE_1_NF * state.device_1_vt));
        double i_dev1 = state.device_1_is * (exp_be_1 - 1.0) * DEVICE_1_SIGN;
        double jdev_1_1 = state.device_1_is / (DEVICE_1_NF * state.device_1_vt) * exp_be_1;

        double vbe_2 = v_d2 * DEVICE_2_SIGN;
        double exp_be_2 = fast_exp(vbe_2 / (DEVICE_2_NF * state.device_2_vt));
        double i_dev2 = state.device_2_is * (exp_be_2 - 1.0) * DEVICE_2_SIGN;
        double jdev_2_2 = state.device_2_is / (DEVICE_2_NF * state.device_2_vt) * exp_be_2;

        double f0 = i_nl[0] - i_dev0;
        double f1 = i_nl[1] - i_dev1;
        double f2 = i_nl[2] - i_dev2;

        double j00 = 1.0 - jdev_0_0 * k_eff[0][0];
        double j01 = 0.0 - jdev_0_0 * k_eff[0][1];
        double j02 = 0.0 - jdev_0_0 * k_eff[0][2];
        double j10 = 0.0 - jdev_1_1 * k_eff[1][0];
        double j11 = 1.0 - jdev_1_1 * k_eff[1][1];
        double j12 = 0.0 - jdev_1_1 * k_eff[1][2];
        double j20 = 0.0 - jdev_2_2 * k_eff[2][0];
        double j21 = 0.0 - jdev_2_2 * k_eff[2][1];
        double j22 = 1.0 - jdev_2_2 * k_eff[2][2];

        // Solve 3x3 system via inline Gaussian elimination
        std::array<std::array<double, 3>, 3> a = {{{{j00, j01, j02}}, {{j10, j11, j12}}, {{j20, j21, j22}}}};
        std::array<double, 3> b = {{f0, f1, f2}};
        bool singular = false;

        for (size_t col = 0; col < 3; ++col) {
            size_t max_row = col;
            double max_val = std::abs(a[col][col]);
            for (size_t row = col + 1; row < 3; ++row) {
                if (std::abs(a[row][col]) > max_val) {
                    max_val = std::abs(a[row][col]);
                    max_row = row;
                }
            }
            if (max_val < SINGULARITY_THRESHOLD) {
                singular = true;
                break;
            }
            if (max_row != col) {
                std::swap(a[col], a[max_row]);
                std::swap(b[col], b[max_row]);
            }
            double pivot = a[col][col];
            for (size_t row = col + 1; row < 3; ++row) {
                double factor = a[row][col] / pivot;
                for (size_t jj = col + 1; jj < 3; ++jj) {
                    a[row][jj] -= factor * a[col][jj];
                }
                b[row] -= factor * b[col];
            }
        }
        if (!singular) {
            for (size_t i_rev = 0; i_rev < 3; ++i_rev) {
                size_t i = 2 - i_rev;
                double sum = b[i];
                for (size_t jj = i + 1; jj < 3; ++jj) {
                    sum -= a[i][jj] * b[jj];
                }
                if (std::abs(a[i][i]) < SINGULARITY_THRESHOLD) {
                    singular = true;
                    break;
                }
                b[i] = sum / a[i][i];
            }
        }
        if (!singular) {
            double delta0 = b[0];
            double delta1 = b[1];
            double delta2 = b[2];

            double dv0 = -(k_eff[0][0] * delta0 + k_eff[0][1] * delta1 + k_eff[0][2] * delta2);
            double dv1 = -(k_eff[1][0] * delta0 + k_eff[1][1] * delta1 + k_eff[1][2] * delta2);
            double dv2 = -(k_eff[2][0] * delta0 + k_eff[2][1] * delta1 + k_eff[2][2] * delta2);
            std::array<double, 3> alpha_arr = {{1.0, 1.0, 1.0}};
            bool any_limited = false;

            if (std::abs(dv0) > 1e-15) {
                double v_lim = pnjlim(v_d0 + dv0, v_d0, state.device_0_n_vt, DEVICE_0_VCRIT);
                double ratio = std::max((v_lim - v_d0) / dv0, 0.01);
                if (ratio < alpha_arr[0]) {
                    alpha_arr[0] = ratio;
                    if (ratio < 1.0) {
                        any_limited = true;
                    }
                }
            }
            if (std::abs(dv1) > 1e-15) {
                double v_lim = pnjlim(v_d1 + dv1, v_d1, state.device_1_vt, DEVICE_1_VCRIT);
                double ratio = std::max((v_lim - v_d1) / dv1, 0.01);
                if (ratio < alpha_arr[1]) {
                    alpha_arr[1] = ratio;
                    if (ratio < 1.0) {
                        any_limited = true;
                    }
                }
            }
            if (std::abs(dv2) > 1e-15) {
                double v_lim = pnjlim(v_d2 + dv2, v_d2, state.device_2_vt, DEVICE_2_VCRIT);
                double ratio = std::max((v_lim - v_d2) / dv2, 0.01);
                if (ratio < alpha_arr[2]) {
                    alpha_arr[2] = ratio;
                    if (ratio < 1.0) {
                        any_limited = true;
                    }
                }
            }

            // Global voltage backstop
            double max_dv = std::max({
                std::abs(dv0 * alpha_arr[0]),
                std::abs(dv1 * alpha_arr[1]),
                std::abs(dv2 * alpha_arr[2])
            });
            if (max_dv > 3.5) {
                double factor = std::max(3.5 / max_dv, 0.1);
                for (auto& aa : alpha_arr) {
                    aa *= factor;
                }
            }
            i_nl[0] -= alpha_arr[0] * delta0;
            i_nl[1] -= alpha_arr[1] * delta1;
            i_nl[2] -= alpha_arr[2] * delta2;

            // Convergence check
            if (!any_limited) {
                bool nr_converged = true;
                {
                    double step = dv0 * alpha_arr[0];
                    double v_new = v_d0 + step;
                    double threshold = 1e-3 * std::max(std::abs(v_d0), std::abs(v_new)) + 1e-6;
                    if (std::abs(step) > threshold) {
                        nr_converged = false;
                    }
                }
                {
                    double step = dv1 * alpha_arr[1];
                    double v_new = v_d1 + step;
                    double threshold = 1e-3 * std::max(std::abs(v_d1), std::abs(v_new)) + 1e-6;
                    if (std::abs(step) > threshold) {
                        nr_converged = false;
                    }
                }
                {
                    double step = dv2 * alpha_arr[2];
                    double v_new = v_d2 + step;
                    double threshold = 1e-3 * std::max(std::abs(v_d2), std::abs(v_new)) + 1e-6;
                    if (std::abs(step) > threshold) {
                        nr_converged = false;
                    }
                }
                if (nr_converged) {
                    state.last_nr_iterations = static_cast<uint32_t>(iter);
                    return i_nl;
                }
            }
        } else {
            // Singular Jacobian — damped fallback
            i_nl[0] -= std::clamp(f0 * 0.5, -0.01, 0.01);
            i_nl[1] -= std::clamp(f1 * 0.5, -0.01, 0.01);
            i_nl[2] -= std::clamp(f2 * 0.5, -0.01, 0.01);
        }
    }

    // Max iterations reached
    state.last_nr_iterations = static_cast<uint32_t>(MAX_ITER);
    if (!std::isfinite(i_nl[0])) { i_nl[0] = 0.0; }
    if (!std::isfinite(i_nl[1])) { i_nl[1] = 0.0; }
    if (!std::isfinite(i_nl[2])) { i_nl[2] = 0.0; }

    return i_nl;
}

inline std::array<double, N> compute_final_voltages(
    const std::array<double, N>& v_pred,
    const std::array<double, M>& i_nl,
    const CircuitState& state)
{
    std::array<double, N> v = v_pred;
    for (size_t i = 0; i < N; ++i) {
        for (size_t j = 0; j < M; ++j) {
            v[i] += state.s_ni[i][j] * i_nl[j];
        }
    }
    return v;
}

