inline std::pair<std::array<std::array<double, N>, N>, bool> invertN(std::array<std::array<double, N>, N> a)
{
    std::array<std::array<double, 2 * N>, N> aug = {};
    for (size_t i = 0; i < N; ++i) {
        for (size_t j = 0; j < N; ++j) {
            aug[i][j] = a[i][j];
        }
        aug[i][N + i] = 1.0;
    }

    for (size_t col = 0; col < N; ++col) {
        size_t max_row = col;
        double max_val = std::abs(aug[col][col]);
        for (size_t row = col + 1; row < N; ++row) {
            if (std::abs(aug[row][col]) > max_val) {
                max_val = std::abs(aug[row][col]);
                max_row = row;
            }
        }

        if (max_val < 1e-30) {
            std::array<std::array<double, N>, N> result = {};
            for (size_t i = 0; i < N; ++i) {
                result[i][i] = 1.0;
            }
            return {result, true};
        }

        if (max_row != col) {
            std::swap(aug[col], aug[max_row]);
        }

        double pivot = aug[col][col];
        for (size_t row = col + 1; row < N; ++row) {
            double factor = aug[row][col] / pivot;
            for (size_t j = col; j < 2 * N; ++j) {
                aug[row][j] -= factor * aug[col][j];
            }
        }
    }

    for (size_t col_rev = 0; col_rev < N; ++col_rev) {
        size_t col = N - 1 - col_rev;
        double pivot = aug[col][col];
        if (std::abs(pivot) < 1e-30) {
            std::array<std::array<double, N>, N> result = {};
            for (size_t i = 0; i < N; ++i) {
                result[i][i] = 1.0;
            }
            return {result, true};
        }
        for (size_t j = 0; j < 2 * N; ++j) {
            aug[col][j] /= pivot;
        }
        for (size_t row = 0; row < col; ++row) {
            double factor = aug[row][col];
            for (size_t j = 0; j < 2 * N; ++j) {
                aug[row][j] -= factor * aug[col][j];
            }
        }
    }

    std::array<std::array<double, N>, N> result = {};
    for (size_t i = 0; i < N; ++i) {
        for (size_t j = 0; j < N; ++j) {
            result[i][j] = aug[i][N + j];
        }
    }
    return {result, false};
}

