inline void CircuitState::setSampleRate(double sample_rate)
{
    if (!(sample_rate > 0.0 && std::isfinite(sample_rate))) {
        return;
    }

    if (std::abs(sample_rate - SAMPLE_RATE) < 0.5) {
        s = S_DEFAULT;
        a_neg = A_NEG_DEFAULT;
        k = K_DEFAULT;
        s_ni = S_NI_DEFAULT;

        pot_0_su = POT_0_SU;
        pot_0_usu = POT_0_USU;
        pot_0_nv_su = POT_0_NV_SU;
        pot_0_u_ni = POT_0_U_NI;

        return;
    }

    double internal_rate = sample_rate * static_cast<double>(OVERSAMPLING_FACTOR);
    double alpha = 2.0 * internal_rate;

    // Build A = G + alpha*C
    std::array<std::array<double, N>, N> a_mat = {};
    for (size_t i = 0; i < N; ++i) {
        for (size_t j = 0; j < N; ++j) {
            a_mat[i][j] = G[i][j] + alpha * C[i][j];
        }
    }

    // Build A_neg = alpha*C - G
    std::array<std::array<double, N>, N> a_neg_new = {};
    for (size_t i = 0; i < N; ++i) {
        for (size_t j = 0; j < N; ++j) {
            a_neg_new[i][j] = alpha * C[i][j] - G[i][j];
        }
    }

    // Invert A to get S
    auto [s_new, singular] = invertN(a_mat);
    if (singular) {
        diag_singular_matrix_count += 1;
    }

    // Compute S * N_i product (N x M)
    std::array<std::array<double, M>, N> s_ni_new = {};
    for (size_t i = 0; i < N; ++i) {
        for (size_t j = 0; j < M; ++j) {
            double sum = 0.0;
            for (size_t kk = 0; kk < N; ++kk) {
                sum += s_new[i][kk] * N_I[j][kk];
            }
            s_ni_new[i][j] = sum;
        }
    }

    // Compute K = N_v * S_NI (M x M)
    std::array<std::array<double, M>, M> k_new = {};
    for (size_t i = 0; i < M; ++i) {
        for (size_t j = 0; j < M; ++j) {
            double sum = 0.0;
            for (size_t n_idx = 0; n_idx < N; ++n_idx) {
                sum += N_V[i][n_idx] * s_ni_new[n_idx][j];
            }
            k_new[i][j] = sum;
        }
    }

    s = s_new;
    a_neg = a_neg_new;
    k = k_new;
    s_ni = s_ni_new;

    // Recompute Sherman-Morrison vectors for pots
    {
        std::array<double, N> u = {};
        if (POT_0_NODE_P > 0) {
            u[POT_0_NODE_P - 1] = 1.0;
        }
        if (POT_0_NODE_Q > 0) {
            u[POT_0_NODE_Q - 1] = -1.0;
        }

        std::array<double, N> su = {};
        for (size_t i = 0; i < N; ++i) {
            double sum = 0.0;
            for (size_t j = 0; j < N; ++j) {
                sum += s[i][j] * u[j];
            }
            su[i] = sum;
        }

        double usu = 0.0;
        for (size_t i = 0; i < N; ++i) {
            usu += u[i] * su[i];
        }

        std::array<double, M> nv_su = {};
        for (size_t i = 0; i < M; ++i) {
            double sum = 0.0;
            for (size_t j = 0; j < N; ++j) {
                sum += N_V[i][j] * su[j];
            }
            nv_su[i] = sum;
        }

        std::array<double, M> u_ni = {};
        for (size_t j = 0; j < M; ++j) {
            double sum = 0.0;
            for (size_t i = 0; i < N; ++i) {
                sum += su[i] * N_I[j][i];
            }
            u_ni[j] = sum;
        }

        pot_0_su = su;
        pot_0_usu = usu;
        pot_0_nv_su = nv_su;
        pot_0_u_ni = u_ni;
    }
}

