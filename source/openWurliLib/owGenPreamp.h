// =============================================================================

inline std::pair<double, double> sm_scale_0(const CircuitState& state)
{
    double r = state.pot_0_resistance;
    if (!std::isfinite(r)) {
        return {0.0, 0.0};
    }
    r = std::clamp(r, POT_0_MIN_R, POT_0_MAX_R);
    double delta_g = 1.0 / r - POT_0_G_NOM;
    double denom = 1.0 + delta_g * state.pot_0_usu;
    double scale = (std::abs(denom) > 1e-15) ? delta_g / denom : 0.0;
    return {delta_g, scale};
}

