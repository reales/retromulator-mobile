// =============================================================================
// MAIN PROCESS FUNCTION
// =============================================================================

inline std::array<double, NUM_OUTPUTS> processSample(double input, CircuitState& state)
{
    // Validate input
    input = std::isfinite(input) ? std::clamp(input, -100.0, 100.0) : 0.0;

    // Compute Sherman-Morrison scale factors for potentiometers
    double delta_g_0 = [&]() {
        double r = std::clamp(state.pot_0_resistance, POT_0_MIN_R, POT_0_MAX_R);
        return 1.0 / r - POT_0_G_NOM;
    }();
    std::array<double, N> su_c0 = state.pot_0_su;
    double usu_c0 = su_c0[6];
    double scale_c0 = (std::abs(1.0 + delta_g_0 * usu_c0) > 1e-15)
        ? delta_g_0 / (1.0 + delta_g_0 * usu_c0)
        : 0.0;
    std::array<double, M> nv_su_c0 = state.pot_0_nv_su;
    std::array<double, M> u_ni_c0 = state.pot_0_u_ni;

    // Step 1: Build RHS vector from history and input
    std::array<double, N> rhs = build_rhs(input, state.input_prev, state);

    // Step 1b: A_neg correction for pot conductance change
    double delta_g_0_prev = [&]() {
        double r = std::clamp(state.pot_0_resistance_prev, POT_0_MIN_R, POT_0_MAX_R);
        return 1.0 / r - POT_0_G_NOM;
    }();
    rhs[6] -= delta_g_0_prev * state.v_prev[6];

    // Step 2: Linear prediction: v_pred = S * rhs
    std::array<double, N> v_pred = mat_vec_mul_s(rhs, state);

    // Step 2b: Sherman-Morrison S correction on v_pred
    double su_dot_rhs_0 = 0.0;
    for (size_t _k = 0; _k < N; ++_k) {
        su_dot_rhs_0 += su_c0[_k] * rhs[_k];
    }
    double factor_0 = scale_c0 * su_dot_rhs_0;
    v_pred[0] -= factor_0 * su_c0[0];
    v_pred[1] -= factor_0 * su_c0[1];
    v_pred[2] -= factor_0 * su_c0[2];
    v_pred[3] -= factor_0 * su_c0[3];
    v_pred[4] -= factor_0 * su_c0[4];
    v_pred[5] -= factor_0 * su_c0[5];
    v_pred[6] -= factor_0 * su_c0[6];
    v_pred[7] -= factor_0 * su_c0[7];
    v_pred[8] -= factor_0 * su_c0[8];
    v_pred[9] -= factor_0 * su_c0[9];
    v_pred[10] -= factor_0 * su_c0[10];
    v_pred[11] -= factor_0 * su_c0[11];

    // Step 3: Extract controlling voltages for nonlinear devices
    std::array<double, M> p = extract_controlling_voltages(v_pred);

    // Step 3b: Precompute corrected K matrix for NR solver
    std::array<std::array<double, M>, M> k_eff = state.k;
    k_eff[0][0] -= scale_c0 * nv_su_c0[0] * u_ni_c0[0];
    k_eff[0][1] -= scale_c0 * nv_su_c0[0] * u_ni_c0[1];
    k_eff[0][2] -= scale_c0 * nv_su_c0[0] * u_ni_c0[2];
    k_eff[1][0] -= scale_c0 * nv_su_c0[1] * u_ni_c0[0];
    k_eff[1][1] -= scale_c0 * nv_su_c0[1] * u_ni_c0[1];
    k_eff[1][2] -= scale_c0 * nv_su_c0[1] * u_ni_c0[2];
    k_eff[2][0] -= scale_c0 * nv_su_c0[2] * u_ni_c0[0];
    k_eff[2][1] -= scale_c0 * nv_su_c0[2] * u_ni_c0[1];
    k_eff[2][2] -= scale_c0 * nv_su_c0[2] * u_ni_c0[2];

    // Step 4: Solve nonlinear system via Newton-Raphson
    std::array<double, M> i_nl = solve_nonlinear(p, k_eff, state);

    // Step 5: Compute final node voltages
    std::array<double, N> v = compute_final_voltages(v_pred, i_nl, state);

    // Step 5b: Sherman-Morrison S*N_i correction on final voltages
    double u_ni_dot_inl_0 = 0.0;
    for (size_t _j = 0; _j < M; ++_j) {
        u_ni_dot_inl_0 += u_ni_c0[_j] * i_nl[_j];
    }
    double sni_factor_0 = scale_c0 * u_ni_dot_inl_0;
    v[0] -= sni_factor_0 * su_c0[0];
    v[1] -= sni_factor_0 * su_c0[1];
    v[2] -= sni_factor_0 * su_c0[2];
    v[3] -= sni_factor_0 * su_c0[3];
    v[4] -= sni_factor_0 * su_c0[4];
    v[5] -= sni_factor_0 * su_c0[5];
    v[6] -= sni_factor_0 * su_c0[6];
    v[7] -= sni_factor_0 * su_c0[7];
    v[8] -= sni_factor_0 * su_c0[8];
    v[9] -= sni_factor_0 * su_c0[9];
    v[10] -= sni_factor_0 * su_c0[10];
    v[11] -= sni_factor_0 * su_c0[11];

    // Step 6: Update history (no-op)
    update_history(v, state);

    // Step 6c: Global node voltage damping safety net
    {
        double max_delta = 0.0;
        for (size_t i = 0; i < 11; ++i) {
            double d = std::abs(v[i] - state.v_prev[i]);
            if (d > max_delta) {
                max_delta = d;
            }
        }
        double max_dc = 0.0;
        for (size_t i = 0; i < 11; ++i) {
            double a = std::abs(state.dc_operating_point[i]);
            if (a > max_dc) {
                max_dc = a;
            }
        }
        double damp_thresh = max_dc * 0.05 + 2.0;
        if (max_delta > damp_thresh) {
            double damp = std::max(damp_thresh / max_delta, 0.01);
            for (size_t i = 0; i < N; ++i) {
                v[i] = state.v_prev[i] + damp * (v[i] - state.v_prev[i]);
            }
            for (size_t i = 0; i < M; ++i) {
                i_nl[i] = state.i_nl_prev[i] + damp * (i_nl[i] - state.i_nl_prev[i]);
            }
        }
    }

    // Step 7: Check for NaN/inf BEFORE updating state
    {
        bool all_finite = true;
        for (size_t i = 0; i < N; ++i) {
            if (!std::isfinite(v[i])) {
                all_finite = false;
                break;
            }
        }
        if (!all_finite) {
            state.v_prev = state.dc_operating_point;
            state.i_nl_prev = {};
            state.i_nl_prev_prev = {};
            state.input_prev = 0.0;
            state.pot_0_resistance = 9.99999999999999854e4;
            state.pot_0_resistance_prev = 9.99999999999999854e4;
            state.diag_nan_reset_count += 1;
            return {{0.0}};
        }
    }

    // Update state for next iteration
    state.v_prev = v;
    state.i_nl_prev_prev = state.i_nl_prev;
    state.i_nl_prev = i_nl;
    state.input_prev = input;
    state.pot_0_resistance_prev = state.pot_0_resistance;

    if (state.last_nr_iterations >= static_cast<uint32_t>(200)) {
        state.diag_nr_max_iter_count += 1;
    }

    // Step 9: Extract outputs and scale
    std::array<double, NUM_OUTPUTS> output = {};
    for (size_t out_idx = 0; out_idx < NUM_OUTPUTS; ++out_idx) {
        double raw_out = v[OUTPUT_NODES[out_idx]];
        raw_out = std::isfinite(raw_out) ? raw_out : 0.0;
        double scaled = raw_out * OUTPUT_SCALES[out_idx];
        double abs_out = std::abs(scaled);
        if (abs_out > state.diag_peak_output) {
            state.diag_peak_output = abs_out;
        }
        output[out_idx] = std::isfinite(scaled) ? scaled : 0.0;
    }
    return output;
}

